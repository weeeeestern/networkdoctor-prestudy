// collectRcodeCounters exports one Prometheus sample per DNS rcode bucket.
func collectRcodeCounters(ch chan<- prometheus.Metric, desc *prometheus.Desc, m *ebpf.Map) {
	for rcode := uint32(0); rcode < 16; rcode++ {
		total := sumU64PerCPU(m, rcode)
		ch <- prometheus.MustNewConstMetric(desc, prometheus.CounterValue, float64(total), fmt.Sprint(rcode))
	}
}

// collectLog2Histogram converts the BPF program's fixed log2 bucket array into
// a Prometheus histogram. Bucket boundaries are powers of two.
func collectLog2Histogram(ch chan<- prometheus.Metric, desc *prometheus.Desc, m *ebpf.Map) {
	counts := make([]uint64, ndHistBuckets)
	var total uint64
	var estimatedSum float64

	for i := uint32(0); i < ndHistBuckets; i++ {
		count := sumU64PerCPU(m, i)
		counts[i] = count
		total += count

		// The BPF program stores only log2 buckets, not exact samples. Use the
		// bucket upper bound as a conservative sum estimate for _sum.
		estimatedSum += float64(count) * float64(log2UpperBound(i))
	}

	cumulative := make(map[float64]uint64, ndHistBuckets-1)
	var running uint64
	for i := uint32(0); i < ndHistBuckets-1; i++ {
		running += counts[i]
		cumulative[float64(log2UpperBound(i))] = running
	}

	ch <- prometheus.MustNewConstHistogram(desc, total, estimatedSum, cumulative)
}

// log2UpperBound returns the inclusive upper bound represented by a log2 bucket
// index used in the BPF histogram arrays.
func log2UpperBound(bucket uint32) uint64 {
	if bucket >= 63 {
		return math.MaxUint64
	}
	return uint64(1) << bucket
}

// sumU64PerCPU reads a per-CPU uint64 array value and sums all CPU slots.
func sumU64PerCPU(m *ebpf.Map, key uint32) uint64 {
	values, err := lookupPerCPU[uint64](m, key)
	if err != nil {
		log.Printf("collect %s[%d]: %v", m.String(), key, err)
		return 0
	}

	var total uint64
	for _, v := range values {
		total += v
	}
	return total
}

// lookupPerCPU handles per-CPU map reads for generated BPF value types. The
// runtime.NumCPU-sized slice works on most systems; the fallback lets cilium/ebpf
// size the slice dynamically if the kernel reports a different CPU count.
func lookupPerCPU[T any](m *ebpf.Map, key uint32) ([]T, error) {
	values := make([]T, runtime.NumCPU())
	if err := m.Lookup(&key, &values); err == nil {
		return values, nil
	}

	var dynamic []T
	if err := m.Lookup(&key, &dynamic); err != nil {
		return nil, err
	}
	return dynamic, nil
}

// ipv4String formats a uint32 IPv4 address from BPF event/map data for logs.
func ipv4String(ip uint32) string {
	var b [4]byte
	binary.LittleEndian.PutUint32(b[:], ip)
	return net.IP(b[:]).String()
}
